const iterateProps = row => {
  var str = "";
  for (const k in row) {
    if (row.hasOwnProperty(k)) {
      if (str.length > 0) str += "; ";
      str += row[k];
    }
  }
  return str;
};

const getResults = results => {
  if (results) {
    return results.map(r => <p>{iterateProps(r)}</p>);
  } else {
    return "No results";
  }
};

